# Tuples are almost like List.
# Tuples have round brackets().
# Tuples are immutable and cannot be modified.
# Why use tuple: Tuples are more efficient than List in terms of memory use and performace .

tuple = (1,2,3,4,5)
print(tuple)

# Tuple does mapping if we put tuple on left side.
(name,age) = ("Shubham",23)
print(name)
print(age)

# Tuples are comparable
value  = (1,2,3)<(4,5,6)
print(value)