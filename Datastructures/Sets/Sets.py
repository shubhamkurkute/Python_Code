# A Set is a unordered, unchangeable and unindexed.
# Unchangeable means you cannot replace the element of set but can be deleted or added.
# Sets doesnt allow duplicate data.

thisset = {"apple", "banana", "cherry", "apple"}        # Second apple is not printed as it is a duplicate data.
print(thisset)


difset = {"Shubam", 20, 5.0}        # Set can contain elements of different datatype.
print(difset)


# Constructor to declare a set.
newset = set(("Shubham", "Raj", "Abhishek"))
print(newset)