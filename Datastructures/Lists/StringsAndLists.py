# split() function breaks the string or sentence and return it in the form of list.
greetings = "How are you"
greet = greetings.split()
print(greet)

# split() function takes arguments which works as a delimiter.
# If no argument is specified it takes space as a one delimiter.
conversation = "What;are;you;doing"
splitedCon = conversation.split(";")
print(splitedCon)