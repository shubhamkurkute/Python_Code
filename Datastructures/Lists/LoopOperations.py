# This section includes loop operations on list.

# Concatenating lists.
a = [1,2,3,4,5]
b = [6,7,8,9,10]
c = a+b
print(c)

# Slicing of list
# Same As string upto but not including number after colon ":".

# Start from 1 upto 3 but not including 3.
print(c[1:3])

# Start from 0 but upto 4 but not including 4.
print(c[:4])